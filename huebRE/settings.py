def ensure_mysql_database():
    """确保 MySQL 数据库存在，如果不存在则创建"""
    connection = None
    try:
        connection = pymysql.connect(
            host=MYSQL_CONNECTION['host'],
            user=MYSQL_CONNECTION['user'],
            password=MYSQL_CONNECTION['password'],
            port=MYSQL_CONNECTION['port'],
            charset=MYSQL_CONNECTION['charset'],
            autocommit=True,
        )
        with connection.cursor() as cursor:
            cursor.execute(
                f"""
                CREATE DATABASE IF NOT EXISTS `{MYSQL_DATABASE_NAME}`
                DEFAULT CHARACTER SET utf8mb4
                COLLATE utf8mb4_unicode_ci
                """
            )
    except pymysql.MySQLError as exc:
        raise ImproperlyConfigured(
            f"无法创建或连接数据库 `{MYSQL_DATABASE_NAME}`: {exc}"
        ) from exc
    finally:
        if connection:
            connection.close()


def ensure_database_and_tables():
    """确保数据库和表存在，如果不存在则创建（运行时调用）"""
    try:
        # 先确保数据库存在
        ensure_mysql_database()
        
        # 检查数据库连接是否正常
        from django.db import connection as db_connection
        db_connection.ensure_connection()
        
        # 检查 user 表是否存在
        with db_connection.cursor() as cursor:
            cursor.execute(
                "SELECT COUNT(*) FROM information_schema.tables "
                "WHERE table_schema = %s AND table_name = 'user'",
                [MYSQL_DATABASE_NAME]
            )
            table_exists = cursor.fetchone()[0] > 0
            
            if not table_exists:
                # 表不存在，运行迁移
                from django.core.management import call_command
                call_command('migrate', verbosity=0, interactive=False)
                
    except Exception as exc:
        # 如果出错，记录但不抛出异常，让视图层处理
        import logging
        logger = logging.getLogger(__name__)
        logger.warning(f"数据库检查/创建失败: {exc}")
        raise  # 重新抛出异常，让调用者知道失败了


