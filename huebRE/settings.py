def env_bool(key: str, default: bool = False) -> bool:
    """Helper to parse boolean env values."""
    value = os.getenv(key)
    if value is None:
        return default
    return value.strip().lower() in {'1', 'true', 'yes', 'on'}


def env_int(key: str, default: int) -> int:
    """Helper to parse integer env values."""
    value = os.getenv(key)
    if value is None:
        return default
    try:
        return int(value)
    except ValueError:
        return default


